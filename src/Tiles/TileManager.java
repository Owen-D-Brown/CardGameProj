    //loads the map from path
    private void loadMapImage() {
        try {
            InputStream is = getClass().getResourceAsStream("/Resources/MapSprite/MMap.png");
            if (is == null) {
                System.out.println("Error: map.png not found in /tiles/");
                return;
            }
            mapImage = ImageIO.read(is);
            System.out.println("Map image loaded successfully.");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
