    // Start collision map based on mapData[][] (1 = walkable, 2 = blocked)
    private void startCMap() {
        for (int row = 0; row < MAP_SIZE; row++) { //row 0 to map size
            for (int col = 0; col < MAP_SIZE; col++) { //col 0 to map size
                cMap[row][col] = (mapData[row][col] == 2); // Blocked if value is 2
            }
        }
    }
    // For debigging and draw grid overlay
    private void drawGrid(Graphics2D g2) {
        g2.setColor(new Color(0, 0, 0, 80)); // Semi-transparent black

        for (int i = 0; i <= MAP_SIZE; i++) { //0 to map size
            int pos = i * TILE_SIZE;	// set the position = i * tilse size
            g2.drawLine(pos, 0, pos, MAP_SIZE * TILE_SIZE); // Vertical lines at pos
            g2.drawLine(0, pos, MAP_SIZE * TILE_SIZE, pos); // Horizontal lines at pos
        }
    }

    public void render(Graphics2D g2) {
        if (mapImage != null) {
            g2.drawImage(mapImage, 0, 0, null);
        } else {
            System.out.println("Error: mapImage null");
        }

        // Draw overlay (for debugging)
        g2.setColor(new Color(255, 0, 0, 150)); // Red semi-transparent
        for (int row = 0; row < MAP_SIZE; row++) { //row 0 to mapsize
            for (int col = 0; col < MAP_SIZE; col++) {  // col to mapsize
                if (cMap[row][col]) { //if row and col are true then draw the red overaly
                    g2.fillRect(col * TILE_SIZE, row * TILE_SIZE, TILE_SIZE, TILE_SIZE);
                }
            }
        }

        //Draw grid for better visibility
        drawGrid(g2);
    }
